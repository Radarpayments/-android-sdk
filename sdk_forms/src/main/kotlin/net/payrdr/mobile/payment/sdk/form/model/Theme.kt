package net.payrdr.mobile.payment.sdk.form.model

/**
 * Possible use cases for themes.
 */
enum class Theme {

    /**
     * The current theme of the application.
     */
    DEFAULT,

    /**
     * Light theme.
     */
    LIGHT,

    /**
     * Dark theme.
     */
    DARK,

    /**
     * According to the theme of the system.
     */
    SYSTEM
}
